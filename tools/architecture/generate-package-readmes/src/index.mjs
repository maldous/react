#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

function parseArgs(argv) {
  const options = {
    root: null,
    format: "text",
    noReports: false,
    write: false,
    packageName: null,
    roots: [],
  };

  let i = 0;
  while (i < argv.length) {
    const arg = argv[i];
    if (arg === "--root") {
      options.root = argv[i + 1];
      i += 2;
    } else if (arg === "--format") {
      options.format = argv[i + 1] ?? "text";
      i += 2;
    } else if (arg === "--no-reports") {
      options.noReports = true;
      i += 1;
    } else if (arg === "--write") {
      options.write = true;
      i += 1;
    } else if (arg === "--check") {
      options.write = false;
      i += 1;
    } else if (arg === "--package") {
      options.packageName = argv[i + 1];
      i += 2;
    } else if (arg.startsWith("--")) {
      throw new Error(`Unknown option: ${arg}`);
    } else {
      options.roots.push(arg);
      i += 1;
    }
  }

  if (!["text", "json"].includes(options.format)) {
    throw new Error("--format must be text or json");
  }

  return options;
}

const OPTIONS = parseArgs(process.argv.slice(2));
const REPO_ROOT = findRepoRoot(OPTIONS.root ? path.resolve(OPTIONS.root) : process.cwd());
const TOOLING_REPORT_DIR = path.join(REPO_ROOT, "reports", "tooling", "generate-package-readmes");
const TOOL_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const TOOL_PACKAGE_PATH = path.join(TOOL_ROOT, "package.json");

function findRepoRoot(startDir) {
  let dir = path.resolve(startDir);
  while (true) {
    if (fs.existsSync(path.join(dir, "docs", "schemas", "package-json-architecture.schema.json"))) {
      return dir;
    }
    const parent = path.dirname(dir);
    if (parent === dir) {
      return path.resolve(startDir);
    }
    dir = parent;
  }
}

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

function isReadmeFixtureDirectory(directoryPath) {
  const parts = directoryPath.split(path.sep);
  return parts.includes("tests") && parts.includes("fixtures");
}

function walkReadmes(current, results, ignored, explicitFixtureScan) {
  const stat = fs.statSync(current);
  if (stat.isDirectory()) {
    const base = path.basename(current);
    if (ignored.has(base)) return;
    if (!explicitFixtureScan && isReadmeFixtureDirectory(current)) return;
    for (const entry of fs.readdirSync(current)) {
      walkReadmes(path.join(current, entry), results, ignored, explicitFixtureScan);
    }
    return;
  }
  if (path.basename(current) === "package.json") {
    results.push(current);
  }
}

function listPackageJsonFiles(searchRoots) {
  const ignored = new Set(["node_modules", ".git", "dist", "build", "coverage", "reports"]);
  const results = [];
  const explicitFixtureScan = searchRoots.some((root) => root.split(/[\\/]/).includes("fixtures"));

  for (const root of searchRoots) {
    const absoluteRoot = path.resolve(REPO_ROOT, root);
    if (!fs.existsSync(absoluteRoot)) {
      continue;
    }
    walkReadmes(absoluteRoot, results, ignored, explicitFixtureScan);
  }

  return [...new Set(results)].sort();
}

function listItems(items) {
  if (!Array.isArray(items) || items.length === 0) {
    return "- None recorded.";
  }
  return items.map((item) => `- ${item}`).join("\n");
}

function codeList(items) {
  if (!Array.isArray(items) || items.length === 0) {
    return "None";
  }
  return items.join("\n");
}

